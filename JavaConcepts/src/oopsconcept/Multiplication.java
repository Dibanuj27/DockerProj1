package oopsconcept;

public class Multiplication {

	public void multiply(double d,double e) {
		double c= d*e;
		System.out.println("Result is: "+c);
	}
	
	public void multiply(int a,int b,int c) {
		int d= a*b*c;
		System.out.println("Result is: "+d);
	}

	public void multiply(float a,float b) {
		float c= a*b;
		System.out.println("Result is: "+c);
	}
}
